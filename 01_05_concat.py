print("\n")

name = "Vân"
age = 20

# Concat
print("Cách 1:")
print("Xin chào " + name + ". Bạn năm nay " + str(age) + " tuổi")

# F-string
print("\nCách 2:")
print(f"Xin chào {name}. Bạn năm nay {age} tuổi")

# Multiline F-string
print("\nCách 3:")
print(
    f"""
        Đầu lòng hai ả tố nga
    Thuý Kiều là chị, em là Thuý {name}.
    """
)
